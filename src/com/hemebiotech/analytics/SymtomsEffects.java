package com.hemebiotech.analytics;

public class SymtomsEffects {

	private int headacheCount = 0;	
	private int rashCount = 0;		
	private int pupilCount = 0;	
	
	public int getHeadacheCount() {
		return headacheCount;
	}
	public void setHeadacheCount(int headacheCount) {
		this.headacheCount = headacheCount;
	}
	public int getRashCount() {
		return rashCount;
	}
	public void setRashCount(int rashCount) {
		this.rashCount = rashCount;
	}
	public int getPupilCount() {
		return pupilCount;
	}
	public void setPupilCount(int pupilCount) {
		this.pupilCount = pupilCount;
	}
	
}
